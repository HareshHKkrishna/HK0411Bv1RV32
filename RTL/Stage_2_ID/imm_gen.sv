`timescale 1ns/1ns
module imm_gen (

    input  logic [31:0] ir,
    output logic [31:0] imm

);

logic [6:0] opcode;

assign opcode = ir[6:0];

always_comb begin

    imm = 32'd0;

    case(opcode)

        //--------------------------------------------------
        // I-Type (ADDI, ANDI, ORI, XORI, LW, JALR ...)
        //--------------------------------------------------
        7'b0010011,
        7'b0000011 :
        begin
            imm = {{20{ir[31]}}, ir[31:20]};
        end

        //--------------------------------------------------
        // S-Type (SB, SH, SW)
        //--------------------------------------------------
        7'b0100011 :
        begin
            imm = {
                {20{ir[31]}},
                ir[31:25],
                ir[11:7]
            };
        end

        //--------------------------------------------------
        // B-Type (BEQ, BNE, BLT ...)
        //--------------------------------------------------
        7'b1100011 :
        begin
            imm = {
                {19{ir[31]}},
                ir[31],
                ir[7],
                ir[30:25],
                ir[11:8],
                1'b0
            };
        end

        //--------------------------------------------------
        // U-Type (LUI)
        //--------------------------------------------------
        7'b0110111 :
        begin
            imm = {
                ir[31:12],
                12'b0
            };
        end

      
        //--------------------------------------------------
        // J-Type (JAL)
        //--------------------------------------------------
        7'b1101111 :
        begin
            imm = {
                {11{ir[31]}},
                ir[31],
                ir[19:12],
                ir[20],
                ir[30:21],
                1'b0
            };
        end

        default :
            imm = 32'd0;

    endcase

end

endmodule